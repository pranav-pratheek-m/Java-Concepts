public class ConditionalAssignment {
    public static void main(String[] args) {
        boolean trueBool = false;
        boolean falseBool = false;

        if (trueBool == true) {
            System.out.println("trueBool is true");
        } else {
            System.out.println("trueBool is false");
        }

        if (falseBool = true) {
            System.out.println("falseBool is true");
        } else {
            System.out.println("falseBool is false");
        }

    }
}
